        body { font-family: 'Inter', 'Segoe UI', serif; line-height: 1.6; color: #333; max-width: 800px; margin: 40px auto; padding: 20px; }
        .header { text-align: center; border-bottom: 2px solid #333; padding-bottom: 20px; margin-bottom: 40px; }
        .entry { margin-bottom: 50px; page-break-inside: avoid; }
        .entry-title { font-size: 1.8rem; margin-bottom: 5px; color: #000; }
        .entry-meta { font-style: italic; color: #666; font-size: 0.9rem; margin-bottom: 15px; border-bottom: 1px solid #eee; padding-bottom: 5px; }
        .entry-content { font-size: 1.1rem; text-align: justify; }
        .badge { display: inline-block; padding: 2px 8px; font-size: 0.8rem; background: #eee; border-radius: 4px; margin-right: 5px; }
        footer { margin-top: 50px; text-align: center; font-size: 0.8rem; color: #999; }
        @media print {
            .no-print { display: none; }
            body { margin: 0; }
        }
    </style>
</head>
<body onload="window.print()">
    <div class="no-print" style="background: #f8f9fa; padding: 10px; margin-bottom: 20px; border-radius: 5px;">
        <button onclick="window.close()" style="padding: 5px 15px; cursor: pointer;">Close Preview</button>
        <span style="margin-left: 20px;">Use <strong>Ctrl + P</strong> to save as PDF if the dialog doesn't appear.</span>
    </div>

    <div class="header">
        <h1>Life Archive</h1>
        <p>A collection of memories by <?php echo htmlspecialchars($_SESSION['full_name']); ?></p>
        <h2><?php echo $month ? date('F Y', mktime(0,0,0,$month, 1, $year)) : "Year $year"; ?></h2>
    </div>

    <?php if ($entries->num_rows > 0): ?>
        <?php while($row = $entries->fetch_assoc()): ?>
            <div class="entry">
                <div class="entry-title"><?php echo htmlspecialchars($row['title']); ?></div>
                <div class="entry-meta">
                    <?php echo date('l, d F Y', strtotime($row['date_gregorian'])); ?>
                    <?php if ($row['date_hijri']): ?> | <?php echo htmlspecialchars($row['date_hijri']); ?><?php endif; ?>
                    <br>
                    <?php if ($row['mood']): ?><span class="badge">Mood: <?php echo htmlspecialchars($row['mood']); ?></span><?php endif; ?>
                    <?php if ($row['chapter_name']): ?><span class="badge">Chapter: <?php echo htmlspecialchars($row['chapter_name']); ?></span><?php endif; ?>
                    <?php if ($row['collection_name']): ?><span class="badge">Collection: <?php echo htmlspecialchars($row['collection_name']); ?></span><?php endif; ?>
                </div>
                <div class="entry-content">
                    <?php echo $row['content']; ?>
                </div>
            </div>
        <?php endwhile; ?>
    <?php else: ?>
        <p style="text-align: center; color: #666; margin-top: 100px;">No entries found for this period.</p>
    <?php endif; ?>

    <footer>
        Generated by <?php echo APP_NAME; ?> on <?php echo date('d M Y H:i'); ?>
    </footer>
</body>
</html>
<?php
    exit;
}

