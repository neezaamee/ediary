<?php
// diary/export.php
require_once '../config/config.php';
require_once '../config/db.php';
require_once '../includes/functions.php';

if (!isLoggedIn()) {
    redirect('login.php');
}

$user_id = $_SESSION['user_id'];
$entries = [];
$mode = isset($_GET['mode']) ? sanitize($_GET['mode']) : ''; // 'print' or 'view'
$year = isset($_GET['year']) ? (int)$_GET['year'] : date('Y');
$month = isset($_GET['month']) ? (int)$_GET['month'] : 0;

if ($mode == 'print') {
    // Fetch entries for export
    $sql = "SELECT d.*, c.name as chapter_name, col.name as collection_name 
            FROM diary_entries d
            LEFT JOIN chapters c ON d.chapter_id = c.id
            LEFT JOIN collections col ON d.collection_id = col.id
            WHERE d.user_id = ? AND YEAR(d.date_gregorian) = ?";
    
    if ($month > 0) {
        $sql .= " AND MONTH(d.date_gregorian) = $month";
    }
    
    $sql .= " ORDER BY d.date_gregorian ASC";
    
    $stmt = $conn->prepare($sql);
    $stmt->bind_param("ii", $user_id, $year);
    $stmt->execute();
    $entries = $stmt->get_result();
?>
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <title>MyDiary Archive - <?php echo $year . ($month ? "-$month" : ""); ?></title>
